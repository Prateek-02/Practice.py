a = int(input("Enter the first num:"))
b = int(input("Enter the second num:"))

print("The value of",a, "+" , b, "is: ",a+b)
print("The value of",a, "-" , b, "is: ",a-b)
print("The value of",a, "*" , b, "is: ",a*b)
print("The value of",a, "/" , b, "is: ",a/b)
print("The value of",a, "%" , b, "is: ",a%b)